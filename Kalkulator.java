package J25_2;

public class Kalkulator {

    public double suma(double liczba1, double liczba2){

        return liczba1 + liczba2;

        //return suma;
    }

    public double roznica(double liczba1, double liczba2){

        double roznica = liczba1 - liczba2;

        return roznica;
    }

    public double iloczyn(double liczba1, double liczba2){

        double iloczyn = liczba1 * liczba2;

        return iloczyn;
    }

    public double iloraz(double liczba1, double liczba2){

        double iloraz = liczba1 / liczba2;

        return iloraz;
    }
}
